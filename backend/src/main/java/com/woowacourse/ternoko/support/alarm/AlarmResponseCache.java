package com.woowacourse.ternoko.support.alarm;

import org.springframework.stereotype.Component;
import org.springframework.web.context.annotation.RequestScope;

@Component
@RequestScope
public class AlarmResponseCache {

    private AlarmResponse origin;
    private AlarmResponse update;
    private SlackMessageType messageType;

    public AlarmResponse getOrigin() {
        return origin;
    }

    public void setOrigin(final AlarmResponse origin) {
        this.origin = origin;
    }

    public SlackMessageType getMessageType() {
        return messageType;
    }

    public void setMessageType(final SlackMessageType messageType) {
        this.messageType = messageType;
    }

    public AlarmResponse getUpdate() {
        return update;
    }

    public void setUpdate(final AlarmResponse update) {
        this.update = update;
    }
}

